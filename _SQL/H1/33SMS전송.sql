	declare	@actionmax		int						set @actionmax 		= 0
	declare @actiontime		datetime				set @actiontime 	= getdate()
	declare @comment		varchar(80)
	declare @gameid			varchar(20)
	declare @smscount		int						set @smscount 		= 0
	declare	@smsmax			int
	declare @smstime		datetime
	declare @dateid 		varchar(8)				set @dateid 		= Convert(varchar(8),Getdate(),112)		-- 20120819
	declare @goldball		int						set @goldball	 	= 0
	declare @silverball		int						set @silverball 	= 0


Begin
	------------------------------------------------
	--	3-1. 초기화
	------------------------------------------------
	set nocount on

	------------------------------------------------
	--	3-2. 연산수행
	------------------------------------------------
	select
		@gameid		= gameid,
		@goldball	= goldball, 	@silverball = silverball,
		@actioncount = actioncount, @actionmax = actionmax, 	@actiontime = actiontime,
		@smscount	= smscount, 	@smsmax		= smsmax, 		@smstime	= smstime
	from dbo.tUserMaster where gameid = @gameid_ and password = @password_

	------------------------------------------------
	-- SMS 지급 시간
	------------------------------------------------
	declare @nActPerMin bigint,
			@nActCount int,
			@dActTime datetime

	declare @nSmsPerMin bigint,
			@nSmsCount int,
			@dSmsTime datetime
	if(isnull(@gameid, '') != '')
		begin
			if(@actioncount = @actionmax)
				begin
					set @silverball = @silverball + 100
				end

			-- 행동력 지급 시간
			set @nActPerMin = @LOOP_TIME_ACTION						-- 행동력 2분에 한개씩 채워줌
			set @nActCount = datediff(s, @actiontime, getdate())/@nActPerMin
			set @dActTime = DATEADD(s, @nActCount*@nActPerMin, @actiontime)
			set @actioncount = @actioncount + @nActCount
			set @actioncount = case when @actioncount > @actionmax then @actionmax else @actioncount end
			--select 'DEBUG 행동치지급', @actiontime actiontime, getdate() getdate, @nActCount nActCount, @actioncount actioncount, @dActTime dActTime
			set @actiontime = @dActTime

			-- SMS 지급 시간
			set @nSmsPerMin = @LOOP_TIME_SMS
			set @nSmsCount = datediff(s, @smstime, getdate())/@nSmsPerMin
			set @dSmsTime = DATEADD(s, @nSmsCount*@nSmsPerMin, @smstime)
			set @smscount = @smscount + @nSmsCount
			set @smscount = case when @smscount > @smsmax then @smsmax else @smscount end
			--select 'DEBUG 행동치지급', @smstime smstime, getdate() getdate, @nSmsCount nSmsCount, @smscount smscount, @dSmsTime dSmsTime
			set @smstime = @dSmsTime

		end

	------------------------------------------------
	--	3-2-3. 연산수행
	------------------------------------------------
	if(isnull(@gameid, '') = '')
		begin
			-- 아이디가 존재하지않는가??
			set @nResult_ = @RESULT_ERROR_NOT_FOUND_GAMEID
			set @comment = 'ERROR 아이디를 찾지 못했습니다.'
		end
	else if(isnull(@recphone_, '') = '' or isnull(@recphone2_, '') = '' or @recphone_ != @recphone2_)
		begin
			set @nResult_ = @RESULT_ERROR_SMS_NOT_MATCH_PHONE
			set @comment = 'ERROR 전송자 핸드폰이 일치하지 않습니다.'
		end
	else if(exists(select top 1 * from dbo.tSMSBlock where phone = @recphone2_))
		begin
			set @nResult_ = @RESULT_ERROR_SMS_REJECT  		-- 리젝된 폰번호들
			set @comment = 'ERROR 유효하지 않는 폰번호 입니다.'

			--set @nResult_ = @RESULT_ERROR_SMS_KEY_DUPLICATE
			--set @comment = 'ERROR 문자 추천을 잠시후에 사용하여 주세요.2'
		end
	--else if(exists(select * from dbo.tUserSMSLog where recphone = @recphone2_ and senddate > (getdate() - 1)))
	--	begin
	--		--set @nResult_ = @RESULT_ERROR_SMS_DOUBLE_PHONE  	-- 중복전송된 폰번호들
	--		--set @comment = 'ERROR 문자 추천은 1일 1회만 발송 가능합니다.'
	--
	--		set @nResult_ = @RESULT_ERROR_SMS_KEY_DUPLICATE
	--		set @comment = 'ERROR 문자추천 시리얼키가 중복된다.'
	--	end
	else if(exists(select top 1 * from dbo.tUserSMSLog where sendkey = @sendkey_))
		begin
			set @nResult_ = @RESULT_ERROR_SMS_KEY_DUPLICATE
			set @comment = 'ERROR 문자추천 시리얼키가 중복된다.'
		end
	else if(@smscount < 1)
		begin
			set @nResult_ = @RESULT_ERROR_SMS_LACK
			set @comment = 'ERROR 문자 추천을 잠시후에 사용하여 주세요.'
		end
	else
		begin
			set @nResult_ = @RESULT_SUCCESS
			set @comment = 'SUCCESS 문자를 정상처리하다.'

			------------------------------------------------------------------
			-- 유저 정보를 업데이트하기
			------------------------------------------------------------------
			--set @actioncount = @actioncount + 5
			set @actioncount = @actioncount + 1
			set @actioncount = case when @actioncount > @actionmax then @actionmax else @actioncount end
			set @smscount = @smscount - 1

			update dbo.tUserMaster
			set
				actioncount		= case when @actioncount < 0 then 0 else @actioncount end,			-- 행동력 갱신
				actiontime		= @actiontime,
				smscount		= @smscount,			-- sms 갱신
				silverball		= @silverball,
				smstime			= @smstime
			where gameid 		= @gameid_

			---------------------------------------------------
			-- 로그 기록하기
			---------------------------------------------------
			insert into dbo.tUserSMSLog(gameid, sendkey, recphone)
			values(@gameid_, @sendkey_, @recphone_)

			---------------------------------------------------
			-- 토탈 기록하기
			---------------------------------------------------
			if(exists(select top 1 * from dbo.tUserSMSLogTotal where dateid = @dateid))
				begin
					update dbo.tUserSMSLogTotal
						set
							cnt = cnt + 1
					where dateid = @dateid
				end
			else
				begin
					insert into dbo.tUserSMSLogTotal(dateid, cnt)
					values(@dateid, 1)
				end

		end



	------------------------------------------------
	--	4-2. 유저정보
	------------------------------------------------
	select @nResult_ rtn, @comment comment, @smscount smscount, @actioncount actioncount, @actionmax actionmax, @actiontime actiontime, @silverball silverball, @goldball goldball

	set nocount off
End

